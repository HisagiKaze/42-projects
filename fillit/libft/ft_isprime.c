#include "libft.h"

int		ft_isprime(int c)
{
	int		i;

	if (c == 2)
		return (1);
	if (c < 2 || !(c % 2))
		return (0);
	i = 3;
	while (i * i <= c)
	{
		if (!(c % i))
			return (0);
		i = i + 2;
	}
	return (1);
}
